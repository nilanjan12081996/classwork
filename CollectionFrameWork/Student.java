package CollectionFrameWork;

public class Student {
 private int id;
private String name;
private int rollno;

public int getId() {
	return id;
}

public String getName() {
	return name;
}
public int getRollno() {
	return rollno;
}
public Student(int id, String name, int rollno) {
	super();
	this.id = id;
	this.name = name;
	this.rollno = rollno;
}

}
